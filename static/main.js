/**
 * Copy functionality and toast notifications for Secure Intake app
 */

// Toast container for notifications
let toastContainer;

// Initialize when DOM is loaded
document.addEventListener('DOMContentLoaded', function() {
    toastContainer = document.getElementById('toast-container');
});

/**
 * Copy a single field value to clipboard
 * @param {string} fieldKey - The key of the field being copied
 * @param {string} value - The value to copy
 */
function copyField(fieldKey, value) {
    if (!navigator.clipboard) {
        fallbackCopy(value);
        return;
    }
    
    navigator.clipboard.writeText(value).then(function() {
        showToast(`${fieldKey.replace('_', ' ').replace(/\b\w/g, l => l.toUpperCase())} copied!`);
    }).catch(function(err) {
        console.error('Failed to copy field:', err);
        showToast('Failed to copy. Please try again.', 'error');
    });
}

/**
 * Copy all customer data in lender-friendly format
 */
function copyAll() {
    if (typeof window.customerData === 'undefined') {
        showToast('Customer data not available', 'error');
        return;
    }
    
    // Order specified in requirements
    const fieldOrder = [
        'first_name', 'last_name', 'email', 'mobile_phone', 'ssn', 'dob',
        'annual_income', 'employment_status', 'driver_license_number',
        'driver_license_state', 'address_line1', 'address_line2', 'city',
        'state', 'zip', 'project_cost', 'notes'
    ];
    
    // Build formatted text block
    let copyText = 'CUSTOMER INFORMATION\n';
    copyText += '===================\n\n';
    
    fieldOrder.forEach(function(field) {
        const value = window.customerData[field];
        if (value && value.trim() !== '') {
            const label = field.replace('_', ' ').replace(/\b\w/g, l => l.toUpperCase());
            copyText += `${label}: ${value}\n`;
        }
    });
    
    copyText += '\n[Generated by Secure Intake System]';
    
    if (!navigator.clipboard) {
        fallbackCopy(copyText);
        return;
    }
    
    navigator.clipboard.writeText(copyText).then(function() {
        showToast('All customer data copied in lender format!');
    }).catch(function(err) {
        console.error('Failed to copy all data:', err);
        showToast('Failed to copy. Please try again.', 'error');
    });
}

/**
 * Fallback copy method for browsers without clipboard API
 * @param {string} text - Text to copy
 */
function fallbackCopy(text) {
    const textArea = document.createElement('textarea');
    textArea.value = text;
    textArea.style.position = 'fixed';
    textArea.style.left = '-999999px';
    textArea.style.top = '-999999px';
    document.body.appendChild(textArea);
    textArea.focus();
    textArea.select();
    
    try {
        const successful = document.execCommand('copy');
        if (successful) {
            showToast('Copied!');
        } else {
            showToast('Failed to copy. Please select and copy manually.', 'error');
        }
    } catch (err) {
        console.error('Fallback copy failed:', err);
        showToast('Copy not supported. Please select and copy manually.', 'error');
    }
    
    document.body.removeChild(textArea);
}

/**
 * Show a toast notification
 * @param {string} message - Message to show
 * @param {string} type - Type of toast (success, error)
 */
function showToast(message, type = 'success') {
    if (!toastContainer) {
        console.warn('Toast container not found');
        return;
    }
    
    // Create toast element
    const toast = document.createElement('div');
    toast.className = 'custom-toast';
    toast.textContent = message;
    
    // Apply type-specific styling
    if (type === 'error') {
        toast.style.backgroundColor = 'var(--bs-danger)';
    }
    
    // Add to container
    toastContainer.appendChild(toast);
    
    // Trigger show animation
    setTimeout(function() {
        toast.classList.add('show');
    }, 10);
    
    // Remove after delay
    setTimeout(function() {
        toast.classList.remove('show');
        setTimeout(function() {
            if (toastContainer.contains(toast)) {
                toastContainer.removeChild(toast);
            }
        }, 300);
    }, 3000);
}

/**
 * Format currency values for display
 * @param {string|number} value - Numeric value to format
 * @returns {string} Formatted currency string
 */
function formatCurrency(value) {
    const num = parseInt(value);
    if (isNaN(num)) return value;
    return '$' + num.toLocaleString();
}

/**
 * Format phone number for display
 * @param {string} phone - Phone number to format
 * @returns {string} Formatted phone number
 */
function formatPhone(phone) {
    const cleaned = phone.replace(/\D/g, '');
    if (cleaned.length === 10) {
        return `(${cleaned.slice(0, 3)}) ${cleaned.slice(3, 6)}-${cleaned.slice(6)}`;
    }
    return phone;
}

/**
 * Format SSN for display (with masking)
 * @param {string} ssn - SSN to format
 * @param {boolean} mask - Whether to mask the SSN
 * @returns {string} Formatted SSN
 */
function formatSSN(ssn, mask = false) {
    const cleaned = ssn.replace(/\D/g, '');
    if (cleaned.length === 9) {
        if (mask) {
            return `XXX-XX-${cleaned.slice(-4)}`;
        }
        return `${cleaned.slice(0, 3)}-${cleaned.slice(3, 5)}-${cleaned.slice(5)}`;
    }
    return ssn;
}
